  <h3>Acerca De Nosotros</h3>
  <p>La Inmobiliaria HomeState., surgió a partir de la necesidad imperiosa en la ciudad de Ocaña de lograr un mercado organizado en el arrendamiento de inmuebles y comercialización de los mismos.

	Para la década del 80, Ocaña estaba enmarcada dentro de una desorganización inmobiliaria, se carecía absolutamente de la cultura inmobiliaria, por lo que a partir de un rápido mercadeo se generó la necesidad de crear una entidad de derecho privado, que empezara por culturizar a la población para que depositara la confianza y entregara las propiedades a una empresa que se dedicara a esa actividad.

	Fue así como llegó a la ciudad para esa época el Doctor Henry , abogado recién egresado de la Universidad de Antioquia, quien venía impregnado de esa pujanza paisa y conoció en esta ciudad al Doctor Alberto Laino Moreno, para entonces Juez Civil del Circuito de esta ciudad y una vez retirado de la judicatura, emprendieron la tarea de constituir lo que entonces se denomina Inmobiliaria Laino y Solano Lda., sociedad pujante que cada día crece y es orgullo y patrimonio de los Ocañeros, fundada el 11 de agosto de 1982, debidamente constituida mediante Escritura Pública, con permiso de funcionamiento otorgado por la Gobernación del Norte de Santander y hoy afiliada a la Lonja de Propiedad Raíz del Norte de Santander y Arauca, entidad esta que avala la seriedad y responsabilidad de la Inmobiliaria Laino y Solano Ltda.

	Cumplió veinte años de existencia y ha cumplido todo un proceso desde su inicio con las rudimentarias máquinas de escribir, hasta hoy contar con tecnología de punta, red de computadoras y sistematización total que garantiza el exacto manejo de la propiedades de nuestros innumerables clientes y arrendatarios, convirtiéndose en pionera en el ramo de los arrendamientos.

	LA INMOBILIARIA LAINO Y SOLANO LTDA. , genera empleo y progreso para nuestra ciudad, contando con un selecto grupo administrativo de personas altamente calificado; varios de los integrantes son egresadas de la Universidad Francisco de Paula Santander y otras exbancarias de alta calidad, que son orgullo para nuestra empresa y nos han hecho crecer.

	Nuestros clientes son los mejores de la ciudad. Entre ellos existen muchas instituciones que han depositado la confianza en la Inmobiliaria Laino y Solano Ltda., para esta empresa son tan importantes los PROPIETARIOS como los ARRENDATARIOS o INQUILINOS. Todos ellos son los que hacen que esta empresa sea LA MEJOR.

	La INMOBIALIARA LAINO Y SOLANO LTDA. hace parte de la cultura Ocañera.</p>
  
 
